<template>
    <div class="cascader" v-click-outside="close">
        <div class="trigger" @click="toggle">{{result || '&nbsp'}}</div>
        <slot></slot>
        <div class="popover-wrapper" v-if="popoverVisible">
            <g-cascader-items :items="source" :load-data="loadData" class="popover" :height="popoverHeight"
                              :loading-item="loadingItem"
                              @update:selected="upDateSelected" :selected="selected"></g-cascader-items>
        </div>
    </div>
</template>

<script>
    import CascaderItems from './cascader-items'
    import clickOutside from '../click-outside'

    export default {
        name: "GuluCascader",
        components: {
            gCascaderItems: CascaderItems
        },
        directives: {clickOutside},
        props: {
            source: {
                type: Array
            },
            popoverHeight: {
                type: String
            },
            selected: {
                type: Array,
                default: () => []
            },
            loadData: {
                type: Function
            },
        },
        data() {
            return {
                popoverVisible: false,
                loadingItem: {}
            }
        },
        methods: {
            open() {
                this.popoverVisible = true
            },
            close() {
                this.popoverVisible = false
            },
            toggle() {
                if (this.popoverVisible === true) {
                    this.close()
                } else {
                    this.open()
                }
            },
            upDateSelected(newSelected) {
                this.$emit('update:selected', newSelected)
                let lastItem = newSelected[newSelected.length - 1]
                let simplest = (children, id) => {
                    return children.filter((item) => item.id === id)[0]
                }
                let complex = (children, id) => {
                    let noChildren = []
                    let hasChildren = []
                    children.forEach(item => {
                        if (item.children) {
                            hasChildren.push(item)
                        } else {
                            noChildren.push(item)
                        }
                    })
                    let found = simplest(noChildren, id)
                    if (found) {
                        return found
                    } else {
                        found = simplest(hasChildren, id)
                        if (found) {
                            return found
                        } else {
                            for (let i = 0; i < hasChildren.length; i++) {
                                found = complex(hasChildren[i].children, id)
                                if (found) {
                                    return found
                                }
                            }
                            return undefined
                        }

                    }
                }
                let upDateSource = (result) => {
                    this.loadingItem = {}
                    let copy = JSON.parse(JSON.stringify(this.source))
                    let toUpdate = complex(copy, lastItem.id)
                    toUpdate.children = result
                    this.$emit('update:source', copy)
                }
                if (!lastItem.isLeaf && this.loadData) {
                    this.loadData(lastItem, upDateSource)
                    this.loadingItem = lastItem
                }
            },
        },
        computed: {
            result() {
                return this.selected.map(item => item.name).join('/')
            }
        }
    }
</script>

